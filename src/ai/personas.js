function withRules(persona) {
  if (!persona) return null;
  return { ...persona, prompt: persona.prompt + PERSONA_RULES };
}

function getRandomPersona() {
  return withRules(PERSONAS[Math.floor(Math.random() * PERSONAS.length)]);
}

function getPersonaById(id) {
  const p = PERSONAS.find(x => x.id === id);
  return p ? withRules(p) : null;
}

function findPersonaInText(text) {
  if (!text) return null;
  const lower = text.toLowerCase();
  const p = PERSONAS.find(persona => {
    const name = persona.name.toLowerCase();
    const escaped = name.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
    return new RegExp(`(?<![а-яёa-z0-9_])${escaped}(?![а-яёa-z0-9_])`, 'i').test(lower);
  });
  return p ? withRules(p) : null;
}

module.exports = { PERSONAS, getRandomPersona, getPersonaById, findPersonaInText };
