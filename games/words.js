/**
 * Возвращает случайное слово с подсказкой-категорией.
 * @returns {{ word: string, hint: string }}
 */
function getRandomWord() {
  const categories = Object.keys(CATEGORIES);
  const hint = categories[Math.floor(Math.random() * categories.length)];
  const words = CATEGORIES[hint];
  const word = words[Math.floor(Math.random() * words.length)];
  return { word, hint };
}

